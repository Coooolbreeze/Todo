<script>
import TabContainer from './TabContainer.vue'

export default {
  name: 'Tabs',
  components: {
    TabContainer
  },
  props: {
    value: {
      type: [String, Number],
      required: true
    }
  },
  data () {
    return {
      panes: []
    }
  },
  render () {
    return (
      <div class={this.$style.tabs}>
        <ul class={this.$style['tabs-header']}>{this.$slots.default}</ul>
        <tab-container panes={this.panes} />
      </div>
    )
  },
  methods: {
    onChange (index) {
      this.$emit('change', index)
    }
  }
}
</script>

